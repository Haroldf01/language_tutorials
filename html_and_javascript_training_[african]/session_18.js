// Syntax of while loop

/**
 * while (expression) {
 *  statement
 * }
 */

let count = 1;
while (count <= 10) {
    console.log("hey");
    count += 2;
}

// even numbers
let number = 0;
while (number <= 20) {
    console.log(number);
    number += 2;
}

